    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
        i++;
       switch (i) {
        case 1:
            
            mainLabel.setText("Please wait while your installation is validated...");
           // win.displayMessage("Please wait while your installation is validated...");
            
            u.initialValidation();
            
            backButton.setEnabled(true);
           // win.enableAllButtons();
            mainLabel.setText("Click next to contact update server.");
            //win.displayMessage("Installation validated. Click next to contact update server.");
            JOptionPane.showMessageDialog(jPanel2, "Installation validated. Click next to contact update server.","Validation",JOptionPane.PLAIN_MESSAGE);

            break;
        case 2:
            backButton.setEnabled(false);
            nextButton.setEnabled(false);
            cancelButton.setEnabled(false);
            //win.disableAllButtons();
            mainLabel.setText("Cannot contact update server.");
            //win.displayMessage("Contacting the update server...");
            u.fetch();
            if (u.data==null) {
               mainLabel.setText("Cannot contact update server.");
               cancelButton.setEnabled(true);
            } else {
                mainLabel.setText("Update server contacted. Got "+u.data.getNumberOfReleases() +
                                   " releases across " + u.data.getNumberOfBranches() + " branches.");
            backButton.setEnabled(true);
            nextButton.setEnabled(false);
            cancelButton.setEnabled(true);
            }

        }
    }//GEN-LAST:event_nextButtonActionPerformed


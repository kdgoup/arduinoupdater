    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
        // TODO add your handling code here:
        i--;
        switch (i){
            case 0:
                backButton.setEnabled(false);
                mainLabel.setText("Click Next To Start");
                break;
                
            case 1:
                mainLabel.setText("Please wait while your installation is validated...");
           // win.displayMessage("Please wait while your installation is validated...");
            
            u.initialValidation();
            
            backButton.setEnabled(true);
           // win.enableAllButtons();
            mainLabel.setText("Installation validated. Click next to contact update server.");
            //win.displayMessage("Installation validated. Click next to contact update server.");
            nextButton.setEnabled(true);
            break;
                
                    
        }
    }//GEN-LAST:event_backButtonActionPerformed

